            let level = 1 // уровень сложности игры на минимум
            let questionRatingInt = (70..<100).randomElement() ?? 80
            var questionRating    = Float(questionRatingInt) / 10
            switch level {
            // в макете на фигме не предусмотрен дробный вариант :-(
            //  case 3:
            //      text = "Рейтинг этого фильма больше чем \(questionRating)?"
            //      break
            case 2:
                questionRating.round(.down)
                text = "Рейтинг этого фильма больше чем \(Int(questionRating))?"
                break

            default:
                questionRating = 7.0
                text = "Рейтинг этого фильма больше чем \(Int(questionRating))?"
                
            }
            let correctAnswer = rating > Float(questionRating)
*/
            // 100% выигрышный вариант по умолчанию
            let text = "Рейтинг этого фильма больше чем 7?"
            let correctAnswer = rating > 7
            
            let question = QuizQuestion(image: imageData,
                                         text: text,
                                         correctAnswer: correctAnswer)

            DispatchQueue.main.async { [weak self] in
                guard let self = self else { return }
                self.delegate?.didReceiveNextQuestion(question: question)
            }
